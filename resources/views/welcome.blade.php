        <h3>Implementasi Health Safety Security Environment (HSSE)</h3>
        <ul>
            <li>
                Pengelolaan Risiko Kesehatan Kerja<br>
            Sebagai upaya mengelola risiko kesehatan kerja, perusahaan memberikan fasilitas pelayanan dan jaminan Kesehatan melalui Badan Penyelenggara Jaminan Kesehatan (BPJS).
            </li>
            <li>
                Pengelolaan Risiko Keselamatan Kerja<br>
            Dalam upaya meningkatkan keselamatan pekerja di lingkungan pekerja PDAM, perusahaan berkewajiban untuk memberikan fasilitas keamanan berupa alat pengaman dan perlindungan diri yang berguna untuk menunjang keselamatan pekerja di tempat kerja.
            </li>
            <li>
                Pengelolaan Risiko Kecelakaan Kerja<br>
            Untuk mempermudah penanganan akibat kecelakaan kerja yang terjadi maka perusahaan telah mengidentifikasi risiko kecelakaan kerja beserta tatacara penanganannya yang berpedoman pada manajemen HSSE di Indonesia.
            </li>
        </ul>
        <h3>Kebijakan Health Safety Security Environment (HSSE)</h3>
        <ul>
            <li>
                Keselamatan Kerja<br>
            Mentaati peraturan perundang-undangan HSSE yang ada di Indonesia.<br>
            Memberikan tindakan pencegahan untuk meminimalisir keadaan darurat.<br>
            Menyediakan alat perlindungan diri bagi pekerja.<br>
            Melakukan pelatihan guna penanggulangan keadaan darurat secara berkesinambungan.<br>
            Menerapkan evaluasi terhadap pengimplementasian HSSE yang telah dilakukan sebagai pertimbangan pelaksanaan kegiatan keberlanjutan.
            </li>
            <li>
                Kesehatan Kerja<br>
            Dalam upaya meningkatkan kesehatan di lingkungan kerja maka, perusahaan memperhatikan kesehatan pekerja dan tempat kerja.
            </li>
            <li>
                Kesehatan Lingkungan<br>
            Mentaati peraturan perundang-undangan dan pengelolaan dan pelestarian lingkungan.<br>
            Melestarikan lingkungan sekitar tempat kerja.<br>
            Melakukan pengecekan berkala terhadap sarana pengelolaan lingkungan.<br>
            Mengelola limbah yang dihasilkan sesuai prosedur.<br>
            Melakukan penanggulangan dan evaluasi terhadap pencemaran lingkungan yang terjadi.<br>
            </li>
        </ul>
